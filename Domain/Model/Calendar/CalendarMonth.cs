namespace Iwbe.Domain.Model
{
    /// <summary>
    /// Represents a month in a fictional calendar. 
    /// </summary>
    public class CalendarMonth
    {
        /// <summary>
        /// Name of the month. E. g. "January", "May", etc. 
        /// </summary>
        public string Name;

        /// <summary>
        /// The number of days in this month. 
        /// </summary>
        public int NumberOfDays;
    }
}
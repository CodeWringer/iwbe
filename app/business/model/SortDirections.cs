namespace iwbe.business.model
{
    /// <summary>
    /// Represents possible sorting directions. 
    /// </summary>
    public enum SortDirections
    {
        /// <summary>
        /// Results will be sorted in ascending order, starting from the lowest value. 
        /// </summary>
        Ascending,
        /// <summary>
        /// Results will be sorted in descending order, starting from the greatest value. 
        /// </summary>
        Descending
    }
}